public class Point {

    private int width = 0;
    private int height = 0;

    public Point(int theWidth, int theHeight){
        width = theWidth;
        height = theHeight;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int theWidth) {
        this.width = theWidth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int theHeight) {
        this.height = theHeight;
    }

    
}