public class Point {
    private int width;
    private int height;

    public Point(int theWidth, int theHeight){
        width = theWidth;
        height = theHeight;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int theWidth) {
        this.width = theWidth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int theHeight) {
        this.height = theHeight;
    }
}
